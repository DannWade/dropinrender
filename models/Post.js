const mongoose = require("mongoose");
const User = require("../models/User");

const PostSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    require: false,
  },
  cloudinaryId: {
    type: String,
    require: false,
  },
  location: {
    type: String,
    required: true,
  },
  date: {
    type: String,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
  remarks: {
    type: String,
    required: true,
  },
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  players:{
    type: Array,
  },
  likes:{
    type: Number, 
  },
  createdBy:{
    type:String,
  }
});

module.exports = mongoose.model("Post", PostSchema);
